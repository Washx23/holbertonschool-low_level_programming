#include "3-calc.h"

/**
 *op_add - la suma de a y b
 *@a: integrer a
 *@b: integrer b
 *Return: a + b
 */
int op_add(int a, int b)
{
	return(a + b);
}

/**
 *op_sub - la resta de a y b
 *@a: integrer a
 *@b: integrer b
 *Return: a - b
 */

 int op_sub(int a, int b)
{
	return(a - b);
}
/**
 *op_mul - la multiplicacion de a y b
 *@a: integrer a
 *@b: integrer b
 *Return: a * b
 */

 int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - la division entre a y b
 *@a: integrer a
 *@b: integrer b
 *Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
*op_mod - la resta de la division de a dividido b
*@a: integrer a
*@b: integrer b
*Return: a % b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
